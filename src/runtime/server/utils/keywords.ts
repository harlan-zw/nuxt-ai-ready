/**
 * Extract keywords from text using word frequency
 */
export function extractKeywords(text: string, metaKeywords?: string, max = 10): string[] {
  // Use meta keywords if available
  if (metaKeywords) {
    return metaKeywords
      .split(',')
      .map(k => k.trim().toLowerCase())
      .filter(Boolean)
      .slice(0, max)
  }

  if (!text?.trim())
    return []

  // Tokenize: lowercase, split on non-word chars, filter
  const words = text
    .toLowerCase()
    .replace(RE_NON_WORD_CHARS, ' ')
    .split(RE_WHITESPACE)
    .filter(w => w.length > 2 && !STOPWORDS.has(w) && !RE_DIGITS.test(w))

  // Count frequency
  const freq = new Map<string, number>()
  for (const word of words)
    freq.set(word, (freq.get(word) || 0) + 1)

  // Sort by frequency, take top N
  return freq.entries().toSorted((a, b) => b[1] - a[1]).slice(0, max).map(([word]) => word)
}
